<h2>File Status</h2>
<div class="article">
    There are several statuses an uploaded PDF can have:
    <ul>
        <li>
            <div class="term">queued</div>
            <div class="definition">A PDF file is queued to be serviced The time to wait depends on how many PDFs are ahead in the queue.</div>
        </li>
        <li>
            <div class="term">processing</div>
            <div class="definition">The PDF file is being processed.</div>
        </li>
        <li>
            <div class="term">completed</div>
            <div class="definition">The PDF file is completed and is available for download.</div>
        </li>
        <li>
            <div class="term">expired</div>
            <div class="definition">It has been more than 24 hours since the PDF has been processed and has been removed from the system.</div>
        </li>
        <li>
            <div class="term">failed</div>
            <div class="definition">The PDF failed to process. Try re-uploading the file or contact our <a href="mailto:assett@colorado.edu">Technical Staff</a> about a failed file.</div>
        </li>
    </ul>
</div>

<h2>Frequently Asked Questions</h2>
<div class="article">
    <ul>
        <li>
            <div class="term">Can other people access the files that I have uploaded?</div>
            <div class="definition">No. You will be the only person able to access your documents.</div>
        </li>
        <li>
            <div class="term">Does this service allow me to create PDFs?</div>
            <div class="definition">No. The <?php echo Yii::app()->name; ?> Service will only do two things. First it will make the PDF(s) text searchable. Next it will clean, rotate and properly align the PDF file(s). No files are created.</div>
        </li>
        <li>
            <div class="term">What system settings should I have?</div>
            <div class="definition">Your browser should have JavaScript enabled and have an HTML5 compatible browser. (JavaScript is usually enabled by default)</div>
        </li>
        <li>
            <div class="term">What about copyright and fair use?</div>
            <div class="definition">
                By using the <?php echo Yii::app()->name; ?> Service, you acknowledge that you have acquired permission from the copyright owner or that the material qualifies for fair use.<br/>
                <br/>For more information about copyright and fair use in relation to course material, visit: 
                <a href="http://ucblibraries.colorado.edu/reserves/copyright.htm" target="_blank">http://ucblibraries.colorado.edu/reserves/copyright.htm</a>
            </div>
        </li>
        <li>
            <div class="term">Are the PDFs I upload stored on the server?</div>
            <div class="definition">The PDF's are temporarily stored on the server up to 24 hours. After which they expire and will be deleted from the server.</div>
        </li>
        <li>
            <div class="term">My file was deleted somehow. Is there a backup?</div>
            <div class="definition">No. We do not keep backups of PDF's. If your file was deleted, you must re-upload the file.</div>
        </li>
        <li>
            <div class="term">I am not affiliated with the Arts &amp; Sciences college. Can I still use the <?php echo Yii::app()->name; ?> Service?</div>
            <div class="definition">Yes, you may still use the service but will be limited to only a few PDF scans a day. People affiliated with the Arts &amp; Sciences college have no restrictions.</div>
        </li>
    </ul>
</div>

