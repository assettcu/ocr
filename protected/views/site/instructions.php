<h1 name="accessing-service">Instructions for using the <?php echo Yii::app()->name; ?> Service</h1>

<div class="article">
    The <?php echo Yii::app()->name; ?> Service is designed to help clean, rotate, and properly align PDF scans. PDF scans that are very poor quality will not OCR properly. This is an automated system using a third-party OCR application called ABBYY. Use of this service is free for Arts &amp; Science students and faculty.
</div>

<h2>Accessing and Using the <?php echo Yii::app()->name; ?> Service</h2>
<div class="article">
    <ol>
        <li>Click the Login button.</li>
        <li>Enter your CU IdentiKey Username.</li>
        <li>Enter your CU IdentiKey Password.</li>
        <li>You will be taken to the OCR Service page.</li>
        <li>You can either drag one or multiple files into the "Drag files here" box area, or click on the Add Files button.</li>
        <li>File sizes should not exceed 25MB.</li>
        <li>Select the PDF file you would like to upload.</li>
    </ol>
</div>

