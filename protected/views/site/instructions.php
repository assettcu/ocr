<h1 name="accessing-service">Instructions for using the OCR Transcription Service</h1>

<div class="article">
    The OCR Tool is designed to help clean, rotate, and properly align PDF scans. PDF scans that are very poor quality will not OCR properly.
</div>

<h2>Accessing and Using the OCR Service</h2>
<div class="article">
    <ol>
        <li>Click the Login button.</li>
        <li>Enter your CU IdentiKey Username.</li>
        <li>Enter your CU IdentiKey Password.</li>
        <li>You will be taken to the OCR Service page.</li>
        <li>You can either drag one or multiple files into the "Drag files here" box area, or click on the Add Files button.</li>
        <li>File sizes should not exceed 25MB.</li>
        <li>Select the PDF file you would like to upload.</li>
    </ol>
</div>

